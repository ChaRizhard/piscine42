int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
	{
		i++;
	}
	return (s1[i] - s2[i]);
}

#include <stdio.h>
int main(void)
{
    printf("%d\n", ft_strcmp("1234568", "1234568"));
    printf("%d\n", ft_strcmp("1234", "12345678"));
    printf("%d\n", ft_strcmp("123456", "123"));
    printf("%d\n", ft_strcmp("123756", "123456"));
}
