int	ft_iterative_factorial(int nb)
{
	int	i;
	int	r;

	i = 0;
	r = 1;
	if (nb < 0)
	{
		return (0);
	}
	while (++i <= nb)
	{
		r *= i;
	}
	return (r);
}
